package SingleResponsibility;

public class Marker {
    // Invoice HAS-A Marker
    String name;
    String color;
    int year;
    int price;

    public Marker(String name, String color, int year, int price) {
        this.name = name;
        this.color = color;
        this.year = year;
        this.price = price;
    }

}
